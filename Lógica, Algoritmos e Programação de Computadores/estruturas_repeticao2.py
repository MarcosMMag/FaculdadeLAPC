# O FOR é usado quando sabemos a quantidade de repetições que será realizada

# Variável texto
texto = 'Olá, laço for.'

# FOR que irá pegar caractere por caractere da variavel
for item in texto:
    print('Caractere: ' + item)

# FOR que irá pegar variavel numero de 1 a 10
for numero in range(1, 11):
    print('Número do intervalo: ' + str(numero))